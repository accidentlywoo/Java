package swexpert;

public class ComparisonOpTest {
	public static void main(String[] args) {
		int num1 = 9;
		int num2 = 13;
		int num3 = 13;
		
		System.out.println(num1 > num2);
		System.out.println(num1 >= num2);
		System.out.println(num1 < num2);
		System.out.println(num1 <= num2);
		System.out.println(num1 == num2);
		System.out.println(num2 == num3);
		System.out.println(num1 != num2);
	}
}
